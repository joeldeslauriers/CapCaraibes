const header = document.querySelector("[data-header]");
const nav = document.querySelector("[data-nav]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const langButtons = document.querySelectorAll("[data-lang]");

function getValue(path, lang) {
  return path.split(".").reduce((acc, key) => acc && acc[key], translations[lang]);
}

function applyLanguage(lang) {
  document.documentElement.lang = lang === "fr" ? "fr-CA" : lang;
  localStorage.setItem("capcaraibes_lang", lang);
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = getValue(node.dataset.i18n, lang);
    if (value) node.textContent = value;
  });
  document.querySelector("[data-price-img]").src = priceImages[lang];
  langButtons.forEach((button) => button.classList.toggle("is-active", button.dataset.lang === lang));
}

function closeMenu() {
  nav.classList.remove("is-open");
  document.body.classList.remove("menu-open");
  menuToggle.setAttribute("aria-expanded", "false");
}

window.addEventListener("scroll", () => {
  header.classList.toggle("is-scrolled", window.scrollY > 18);
}, { passive: true });

menuToggle.addEventListener("click", () => {
  const isOpen = nav.classList.toggle("is-open");
  document.body.classList.toggle("menu-open", isOpen);
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

nav.addEventListener("click", (event) => {
  if (event.target.closest("a")) closeMenu();
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

const sections = [...document.querySelectorAll("main section[id]")];
const navLinks = [...document.querySelectorAll(".main-nav a")];
const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) return;
    navLinks.forEach((link) => {
      link.classList.toggle("is-active", link.getAttribute("href") === `#${entry.target.id}`);
    });
  });
}, { rootMargin: "-45% 0px -45% 0px", threshold: 0 });
sections.forEach((section) => observer.observe(section));

document.querySelector("[data-form]").addEventListener("submit", (event) => {
  event.preventDefault();
  const form = new FormData(event.currentTarget);
  const subject = encodeURIComponent("Demande de réservation Cap Caraibes");
  const body = encodeURIComponent([
    `Nom: ${form.get("name") || ""}`,
    `Email: ${form.get("email") || ""}`,
    `Téléphone: ${form.get("phone") || ""}`,
    `Date: ${form.get("date") || ""}`,
    `Personnes: ${form.get("people") || ""}`,
    `Service: ${form.get("service") || ""}`,
    "",
    form.get("message") || ""
  ].join("\n"));
  window.location.href = `mailto:capcaraibes.lasterrenas@gmail.com?subject=${subject}&body=${body}`;
});

applyLanguage(localStorage.getItem("capcaraibes_lang") || "fr");
